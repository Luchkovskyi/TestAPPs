<?php

namespace backend\controllers;

use Yii;
use common\models\News;
use common\models\NewsSeach;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;

/**
 * NewsController implements the CRUD actions for News model.
 */
class NewsController extends BaseController
{
    /**
     * @inheritdoc
     */
    public function behaviors()
    {
        return [
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['POST'],
                ],
            ],
        ];
    }

    /**
     * Lists all News models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new NewsSeach();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single News model.
     * @param string $id
     * @return mixed
     */
    public function actionView($id)
    {
        return $this->render('view', [
            'model' => $this->findModel($id),
        ]);
    }

    /**
     * Creates a new News model.
     * If creation is successful, the browser will be redirected to the 'view' page.
     * @return mixed
     */
    public function actionCreate()
    {
        $model = new News();

        if ($model->load(Yii::$app->request->post()) ) {
            $post= Yii::$app->request->post();
            $model->title = $post['News']['title'];
            $model->description = $post['News']['description'];
             if($_FILES['News']['name']['img_url'] != null){
                 move_uploaded_file($_FILES['News']['tmp_name']["img_url"], 'images/'.$_FILES['News']['name']['img_url']);
                 $model->img_url = '/images/' . $_FILES['News']['name']['img_url'];
             }
             $model->save();
             return $this->redirect('index');
        } else {
            return $this->render('create', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Updates an existing News model.
     * If update is successful, the browser will be redirected to the 'view' page.
     * @param string $id
     * @return mixed
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($model->load(Yii::$app->request->post()) ) {
            $post= Yii::$app->request->post();
            $model->title = $post['News']['title'];
            $model->description = $post['News']['description'];
            if($_FILES['News']['name']['img_url'] != null){
                move_uploaded_file($_FILES['News']['tmp_name']["img_url"], 'images/'.$_FILES['News']['name']['img_url']);
                $model->img_url = '/images/' . $_FILES['News']['name']['img_url'];
            }
            $model->save();
            return $this->redirect('index');
        } else {
            return $this->render('create', [
                'model' => $model,
            ]);
        }
    }

    /**
     * Deletes an existing News model.
